n1 = int(input('Primeiro Valor: '))
n2 = int(input('Digite outro Valor: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2

print('A soma vale {}, o produto vale {} e a divisão vale {}'.format(s, m, d))
print('O valor da divisão inteira é {} e a potência dos números é {}!'.format(di, e))
